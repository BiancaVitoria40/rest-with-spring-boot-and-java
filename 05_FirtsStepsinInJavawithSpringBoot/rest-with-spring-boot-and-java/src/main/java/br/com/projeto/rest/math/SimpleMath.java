package br.com.projeto.rest.math;

public class SimpleMath {
	
	public Double sum(Double numberOne, Double numberTwo) {
	      return numberOne + numberTwo;
	}
	
	public Double divisao(Double numberOne, Double numberTwo) {
	      return numberOne / numberTwo;
	}
	
	public Double multiplica(Double numberOne, Double numberTwo) {
	      return numberOne * numberTwo;
	}
	
	public Double subtratic(Double numberOne, Double numberTwo) {
	      return numberOne - numberTwo;
	}

	

}
